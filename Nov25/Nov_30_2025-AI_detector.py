def detect_ai(text):
    # OPTIMIZATION 1: Early Exit
    # Check simple rules first. If these are true, we stop immediately.
    # This saves us from doing the heavy lifting of processing words.
    if text.count("-") >= 2:
        return "AI"
    
    if text.count("(") >= 2:
        return "AI"

    # OPTIMIZATION 2: Word Processing
    # We only reach here if the dash/parens rules failed.
    long_word_count = 0
    words = text.split(" ")

    for word in words:
        # Your preferred method: No imports, standard Python
        clean_word = "".join([char for char in word if char.isalpha()])
        
        if len(clean_word) >= 7:
            long_word_count += 1
            
            # OPTIMIZATION 3: Stop counting once we hit the threshold
            if long_word_count >= 3:
                return "AI"

    return "Human"

